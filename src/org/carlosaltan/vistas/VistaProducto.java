     public void llenarMarca(){
         ControladorProducto marca = new ControladorProducto(); 
        ArrayList<ModeloMarca> listaMarc = marca.getMarca(); 
        jComboBox1.removeAllItems();
        for (int i = 0; i < listaMarc.size(); i++){
            jComboBox1.addItem(new ModeloMarca(listaMarc.get(i).getId_marca(), listaMarc.get(i).getMarca()));
        }  
    }
     
      public void mostrarProductos( ){
       String sql = "select * from vista_Producto";
        Statement st;
        

        Conexion conn = new Conexion();
        Connection con = conn.getConexion();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Producto");
        model.addColumn("Precio ");
        model.addColumn("Marca");
        model.addColumn("Modelo ");
      
        
        jTable1.setModel(model);
        
        String [] datos = new String[5];
        try{
            st = con.createStatement();
            ResultSet rs  = st.executeQuery(sql);
            while(rs.next()){
                  datos[0] = rs.getString(1);
                  datos[1] = rs.getString(2);
                  datos[2] = rs.getString(3);
                  datos[3] = rs.getString(4);
                  datos[4] = rs.getString(5);
                 
                 
                  
                  model.addRow(datos);
            }   
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e + "Error");
        }
    }

